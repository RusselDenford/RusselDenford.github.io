function getFile (type, src) {
	var i, img;
	for (i = 0; i < resources.resArr.length; i++) {
		if (resources.resArr[i].src == src)
			return resources.resArr[i].file;
	}

	loadFile(type, src);
	return getFile(type, src);
}

function loadFile (type, src) {
	var file;
	switch (type) {
		case "img": {
			file = new Image();
			break;
		}

		case "audio": {
			file = new Audio();
			break;
		}
		default: {}
	}

	file.src = src;
	resources.resArr.push({src: src, file: file});
}


(function () {
	var key, pressedKey = {};

	document.addEventListener('keydown', function(e) {
		setStatus(e, true);
	});

	document.addEventListener('keyup', function(e) {
		setStatus(e, false);
	});

	function setStatus (event, status) {
		var code = event.keyCode;

		switch(code) {
			case 27: {
				event.preventDefault();
				key = "ESC";
				break;
			}
			case 32: {
				event.preventDefault();
				key = "SPACE";
				break;
			}
			case 37: {
				event.preventDefault();
				key = "LEFT";
				break;
			}
			case 38: {
				event.preventDefault();
				key = "UP";
				break;
			}
			case 39: {
				event.preventDefault();
				key = "RIGHT";
				break;
			}
			case 40: {
				event.preventDefault();
				key = "DOWN";
				break;
			}
			default: {
				key = String.fromCharCode(code);
			}
		}
		pressedKey[key] = status;
		if(status) {
			window.input.pressedKey = key;
		}
		if(!status) {
			window.input.unpressedKey = key;
			window.input.pressedKey = "";
		}
	}
	window.input = {
		isDown: function (key) {
			return pressedKey[key];
		},
		pressed: false
	};
})();
