function setCookie (name, value) {
      document.cookie = name + "=" + value;
}

function getCookie(name) {
	var cookie = " " + document.cookie;
	var search = " " + name + "=";
	var setStr = null;
	var offset = 0;
	var end = 0;
	if (cookie.length > 0) {
		offset = cookie.indexOf(search);
		if (offset != -1) {
			offset += search.length;
			end = cookie.indexOf(";", offset)
			if (end == -1) {
				end = cookie.length;
			}
			setStr = unescape(cookie.substring(offset, end));
		}
	}
	return(setStr);
}

function checkGameStat () {
	switch(gameStat) {
		case "game": {
			if (!stage.squad.length && !objs.Enemy.length && ifGame && objs.Base[0].state == "unbroken") {
				ifGame = false;
				setTimeout(function () {
					gameStat = "win";
				}, 5000);
			}
			break;
		}
		case "over": {
			if (!objs.GameOverText.length) {
				createGameOverText(286, 600);
			}

			if (totalPoints > hiScore) {
				setCookie("hiScore", totalPoints);
				hiScore = totalPoints;
			}

			gameStat = "waitForMenu";

			setTimeout(function() {
				playSound(sounds.gameOver);
			}, 2600);

			setTimeout(function() {
				curtains.dir = "toClose";
				curtainsMove();
			}, 4500);
			break;
		}
		case "win": {
			if (numOfStage == 15) {
				gameStat = "victory";
				playSound(sounds.won);
				victoryColorAnimate();
				setTimeout(function() {
					curtains.dir = "toClose";
					curtainsMove();
				}, 8400);

			} else {
				gameStat = "showNumOfStage";
				setTimeout(function() {
					curtains.dir = "toClose"
					curtainsMove();
				}, 750);
			}	
			break;
		}

		case "victory": {
			if(curtains.state == "finished") {
				gameStat = "waitForMenu"
			}
			break;
		}

		case "showNumOfStage": {
			if(curtains.state == "finished") {
				numOfStage++;
				curtains.text = true;
				gameStat = "nextStage";	
			}
			break;
		}

		case "startGame": {
			if(curtains.state == "finished") {
				curtains.text = true;
				gameStat = "nextStage";	
			}
			break;
		}

		case "waitForMenu": {
			if(curtains.state == "finished") {
				numOfStage = 1;
				curtains.dir = "toOpen";
				gameStat = "menu";
				curtainsMove();
			}
			break;
		}

		case "escape": {
			if (totalPoints > hiScore) {
				setCookie("hiScore", totalPoints);
				hiScore = totalPoints;
			}

			gameStat = "waitForMenu";
			setTimeout(function() {
				curtains.dir = "toClose";
				curtainsMove();
			}, 500);
			break;
		}

		case "menu": {
			resetGame();
			startGame(false, "menu");
		}

		default: {
		}
	}

	if (gameStat != "game") {
		if (objs.Player[0]) {
			if (!objs.Player[0].guard) objs.Player[0].animProp.loop = false;
		}
	}
}

function renderingLoop () {
	if (window.requestAnimationFrame)
        window.requestAnimationFrame(renderingLoop);
    else if (window.msRequestAnimationFrame)
        window.msRequestAnimationFrame(renderingLoop);
    else if (window.webkitRequestAnimationFrame)
        window.waterbkitRequestAnimationFrame(renderingLoop);
    else if (window.mozRequestAnimationFrame)
        window.mozRequestAnimationFrame(renderingLoop);
    else if (window.oRequestAnimationFrame)
        window.oRequestAnimationFrame(renderingLoop);

    inputHandler();
    clearScene();
    drawScene(canvas, context);
    showStats();
    checkGameStat();
}

